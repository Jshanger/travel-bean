/** Convert a ring of [lon,lat] to an SVG path string in the 360×180 viewBox */
export function ringToPath(ring: Ring): string {
  return (
    ring.map(([lon, lat], i) =>
      `${i === 0 ? 'M' : 'L'}${(lon + 180).toFixed(2)},${(90 - lat).toFixed(2)}`
    ).join(' ') + 'Z'
  );
}

/** Merge all rings of a country into one SVG `d` attribute string */
export function countryToPath(rings: Ring[]): string {
  return rings.map(ringToPath).join(' ');
}
